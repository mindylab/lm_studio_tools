const ASSET_PORT = parseInteger(process.env.LM_WEB_MCP_ASSET_PORT, 8765);
const ASSET_HOST = process.env.LM_WEB_MCP_ASSET_HOST ?? '0.0.0.0';
const ASSET_ROUTE_PREFIX = '/local-web-assets/';
const imageAssets = new Map();
let assetServerPromise = null;

