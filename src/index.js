async function publishImageAsset({ base64, mimeType, label }) {
  if (process.env.LM_WEB_MCP_DISABLE_ASSET_SERVER === '1') {
    return null;
  }

  const bytes = Buffer.from(String(base64 ?? ''), 'base64');
  if (bytes.length === 0) {
    return null;
  }

  const serverInfo = await ensureAssetServer();
  if (!serverInfo) {
    return null;
  }

  pruneImageAssets();
  const extension = imageExtension(mimeType);
  const safeLabel = sanitizeAssetLabel(label || 'image');
  const id = `${Date.now()}-${randomUUID()}-${safeLabel}.${extension}`;
  imageAssets.set(id, {
    bytes,
    mimeType: normalizeImageMimeType(mimeType),
    createdAt: Date.now(),
  });

  return `${serverInfo.baseUrl}${ASSET_ROUTE_PREFIX}${encodeURIComponent(id)}`;
}

async function ensureAssetServer() {
  if (assetServerPromise) {
    return assetServerPromise;
  }

  assetServerPromise = new Promise((resolve) => {
    const server = createServer((request, response) => {
      try {
        const requestUrl = new URL(request.url ?? '/', 'http://localhost');
        if (!requestUrl.pathname.startsWith(ASSET_ROUTE_PREFIX)) {
          response.writeHead(404).end('Not found');
          return;
        }

        const id = decodeURIComponent(requestUrl.pathname.slice(ASSET_ROUTE_PREFIX.length));
        const asset = imageAssets.get(id);
        if (!asset || Date.now() - asset.createdAt > ASSET_TTL_MS) {
          imageAssets.delete(id);
          response.writeHead(404).end('Image expired or not found');
          return;
        }

        response.writeHead(200, {
          'content-type': asset.mimeType,
          'content-length': String(asset.bytes.length),
          'cache-control': 'no-store',
          'access-control-allow-origin': '*',
        });
        response.end(asset.bytes);
      } catch (error) {
        response.writeHead(500).end(errorMessage(error));
      }
    });

    server.on('error', (error) => {
      console.error(`Image asset server could not start on port ${ASSET_PORT}: ${errorMessage(error)}`);
      resolve(null);
    });

    server.listen(ASSET_PORT, ASSET_HOST, () => {
      const baseUrl = resolveAssetBaseUrl(ASSET_PORT);
      if (process.env.LM_WEB_MCP_DEBUG === '1') {
        console.error(`local-web-mcp image asset server is listening at ${baseUrl}`);
      }
      resolve({ baseUrl });
    });
  });

  return assetServerPromise;
}

function pruneImageAssets() {
  const now = Date.now();
  for (const [id, asset] of imageAssets.entries()) {
    if (now - asset.createdAt > ASSET_TTL_MS) {
      imageAssets.delete(id);
    }
  }

  while (imageAssets.size > 100) {
    const oldest = imageAssets.keys().next().value;
    if (!oldest) break;
    imageAssets.delete(oldest);
  }
}

function resolveAssetBaseUrl(port) {
  const publicBaseUrl = process.env.LM_WEB_MCP_PUBLIC_BASE_URL;
  if (publicBaseUrl) {
    return publicBaseUrl.replace(/\/+$/, '');
  }

  return `http://${detectLanAddress()}:${port}`;
}

function detectLanAddress() {
  const interfaces = networkInterfaces();
  for (const addresses of Object.values(interfaces)) {
    for (const address of addresses ?? []) {
      if (address.family === 'IPv4' && !address.internal) {
        return address.address;
      }
    }
  }

  return '127.0.0.1';
}

function sanitizeAssetLabel(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 48) || 'image';
}

function imageExtension(mimeType) {
  const normalized = normalizeImageMimeType(mimeType);
  if (normalized === 'image/jpeg') return 'jpg';
  if (normalized === 'image/webp') return 'webp';
  return 'png';
}

function normalizeImageMimeType(mimeType) {
  const normalized = String(mimeType ?? '').split(';')[0].trim().toLowerCase();
  if (normalized === 'image/jpg') return 'image/jpeg';
  if (normalized === 'image/jpeg' || normalized === 'image/webp' || normalized === 'image/png') {
    return normalized;
  }
  return 'image/png';
}

function parseInteger(value, fallback) {
  const parsed = Number.parseInt(String(value ?? ''), 10);
  return Number.isFinite(parsed) && parsed > 0 ? parsed : fallback;
}

function errorMessage(error) {
  return error instanceof Error ? error.message : String(error);
}

async function main() {
  const transport = new StdioServerTransport();
  await server.connect(transport);
  if (process.env.LM_WEB_MCP_DEBUG === '1') {
    console.error('local-web-mcp is running on stdio');
  }
}

function wrapTool(handler) {
  return async (args) => {
    try {
      return await handler(args);
    } catch (error) {
      const message =
        error instanceof Error ? error.message : String(error);
      console.error('Tool error:', message);
      return {
        isError: true,
        content: [{ type: 'text', text: `Error: ${message}` }],
      };
    }
  };
}

process.on('unhandledRejection', (error) => {
  console.error('Unhandled rejection:', error);
});

process.on('uncaughtException', (error) => {
  console.error('Uncaught exception:', error);
  process.exit(1);
});

main().catch((error) => {
  console.error('Server error:', error);
  process.exit(1);
});
