server.registerTool(
  'web_page_to_images',
  {
    title: 'Web Page To Images',
    description:
      'Load a web page in a browser and capture the rendered page as screenshot image content. Capture the full scrollable page by default; use fullPage=false only when the user explicitly asks for the top/current viewport. Tall full-page captures are split into multiple images.',
    inputSchema: {
      url: z.string().url().describe('The page URL to render and capture.'),
      viewportWidth: z.coerce
        .number()
        .int()
        .min(320)
        .max(3840)
        .optional()
        .describe('Browser viewport width in CSS pixels. Default: 1280.'),
      viewportHeight: z.coerce
        .number()
        .int()
        .min(320)
        .max(2160)
        .optional()
        .describe('Browser viewport height in CSS pixels. Default: 900.'),
      fullPage: z
        .boolean()
        .optional()
        .describe('Legacy option. Full-page capture is forced unless viewportOnly=true is also set. Default: true.'),
      viewportOnly: z
        .boolean()
        .optional()
        .describe('Set true only when the user explicitly asks for only the top/current visible viewport instead of the full scrollable page. Default: false.'),
      waitUntil: z
        .enum(['domcontentloaded', 'load', 'networkidle'])
        .optional()
        .describe('Page load state to wait for before capture. Default: networkidle.'),
      waitAfterLoadMs: z.coerce
        .number()
        .int()
        .min(0)
        .max(10_000)
        .optional()
        .describe('Extra wait after the selected load state, in milliseconds. Default: 1000.'),
      scrollToLoad: z
        .boolean()
        .optional()
        .describe('Whether to scroll through the page first to trigger lazy loading. Default: true.'),
      segmentHeight: z.coerce
        .number()
        .int()
        .min(1_000)
        .max(16_000)
        .optional()
        .describe('Maximum CSS-pixel height per returned screenshot image. Default: 10000.'),
      maxPageHeight: z.coerce
        .number()
        .int()
        .min(1_000)
        .max(100_000)
        .optional()
        .describe('Maximum full-page CSS-pixel height to capture. Default: 50000.'),
      format: z
        .enum(['png', 'jpeg'])
        .optional()
        .describe('Screenshot image format. Default: png.'),
      jpegQuality: z.coerce
        .number()
        .int()
        .min(1)
        .max(100)
        .optional()
        .describe('JPEG quality when format is jpeg. Default: 90.'),
    },
    outputSchema: pageScreenshotsSchema.shape,
  },
  wrapTool(async (args) => {
    const captureArgs = {
      ...args,
      fullPage: args.viewportOnly === true ? false : true,
    };
    delete captureArgs.viewportOnly;
    const result = await captureWebPageToImages(captureArgs);
    const imagesWithAssets = await Promise.all(
      result.images.map(async (image) => ({
        ...image,
        assetUrl: await publishImageAsset({
          base64: image.data,
          mimeType: image.mimeType,
          label: `web-page-to-images-${image.index}`,
        }),
      })),
    );
    const imageContent = imagesWithAssets.flatMap((image) => {
      const content = [
        {
          type: 'image',
          data: image.data,
          mimeType: image.mimeType,
        },
      ];

      if (image.assetUrl) {
        content.push({
          type: 'text',
          text: `Screenshot ${image.index} image URL: ${image.assetUrl}`,
        });
      } else if (image.data.length <= IMAGE_DATA_URL_FALLBACK_MAX_CHARS) {
        content.push({
          type: 'text',
          text: `Screenshot ${image.index} data URL:\ndata:${image.mimeType};base64,${image.data}`,
        });
      }

      return content;
    });

    return {
      content: [
        { type: 'text', text: result.text },
        ...imageContent,
      ],
      structuredContent: {
        url: result.url,
        finalUrl: result.finalUrl,
        title: result.title,
        viewportWidth: result.viewportWidth,
        viewportHeight: result.viewportHeight,
        fullPage: result.fullPage,
        pageWidth: result.pageWidth,
        pageHeight: result.pageHeight,
        capturedHeight: result.capturedHeight,
        format: result.format,
        images: imagesWithAssets.map(({ data, ...image }) => image),
        warning: result.warning,
      },
    };
  }),
);

